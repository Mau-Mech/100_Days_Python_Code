# CONVERTIR UNA TUPLA A LISTA

a = (34, "python", 4.6, "argentina")
list_a = list(a)
print(f"Esta es una la tupla: {a}")
print(f"Esta es una lista: {list_a}")

# CONVERTIR UNA LISTA A TUPLA

b = ["Mauricio", 1, 5, 158, 12.69, "Jimenez"]
tupla = tuple(b)
print(f"Esta es una la tupla: {tupla}")
print(f"Esta es una lista: {b}")
